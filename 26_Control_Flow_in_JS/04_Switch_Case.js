// Basic syntax
// switch (key) {
//     case value:

//         break;

//     default:
//         break;
// }

const month = 1;

switch (month) {
    case 1:
        console.log("January")
        break;
    case 2:
        console.log("February")
        break;
    case 3:
        console.log("March")
        break;

    default:
        console.log("Not a month")
        break;
}